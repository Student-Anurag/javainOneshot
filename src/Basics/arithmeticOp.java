package Basics;

public class arithmeticOp {
    public static void main(String[] args) {
        int x = 6, y = 4;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        // using double datatype
        double a = 6, b =4;
        System.out.println(a/b);
    }
}
